# for 값 in 리스트

상자 = ["사과", "배", "콩", "두부"] # 리스트 

for 값 in 상자:
  print(값) # 사과 배 콩 두부 출력

"""
상자 안에 물건을 전부 까서
사과는 냉장실에
아이스크림은 냉동실에 넣어 주시고,
나머지는 폐기처분 해주세요.

"""

for 값 in 상자:
  if 값 == "사과":
    print("냉장실")
  elif 값 == "아이스크림":
    print("냉동실")
  else :
    print("폐기")

for 물건 in 상자:
  if 물건 == "사과":
    print(f"'{물건}' 냉장실에 넣기")
  elif 물건 == "아이스크림":
    print(f"'{물건}' 냉동실에 넣기")
  else :
    print(f"'{물건}' 폐기 처분")